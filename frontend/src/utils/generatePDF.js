

import jsPDF from "jspdf";


const C = {
  primary:      [37,  99,  235],
  primaryLight: [219, 234, 254],
  danger:       [220, 38,  38],
  dangerLight:  [254, 226, 226],
  warning:      [217, 119, 6],
  warningLight: [254, 243, 199],
  success:      [22,  163, 74],
  successLight: [220, 252, 231],
  orange:       [234, 88,  12],
  orangeLight:  [255, 237, 213],
  teal:         [20,  184, 166],
  gray900:      [17,  24,  39],
  gray700:      [55,  65,  81],
  gray500:      [107, 114, 128],
  gray300:      [209, 213, 219],
  gray100:      [243, 244, 246],
  white:        [255, 255, 255],
};

const getRiskColor = (risk) => {
  switch (risk) {
    case "Critical": return C.danger;
    case "High":     return C.orange;
    case "Medium":   return C.warning;
    case "Low":      return C.success;
    default:         return C.gray500;
  }
};

const getRiskBg = (risk) => {
  switch (risk) {
    case "Critical": return C.dangerLight;
    case "High":     return C.orangeLight;
    case "Medium":   return C.warningLight;
    case "Low":      return C.successLight;
    default:         return C.gray100;
  }
};

const getConfidenceColor = (confidence) => {
  if (confidence >= 80) return C.success;
  if (confidence >= 60) return C.warning;
  return C.orange;
};


const drawRect = (doc, x, y, w, h, r, fill, stroke) => {
  doc.setFillColor(...fill);
  if (stroke) {
    doc.setDrawColor(...stroke);
    doc.setLineWidth(0.3);
    doc.roundedRect(x, y, w, h, r, r, "FD");
  } else {
    doc.roundedRect(x, y, w, h, r, r, "F");
  }
};

const addHeader = (doc, pw) => {
  doc.setFillColor(...C.primary);
  doc.rect(0, 0, pw, 26, "F");

  doc.setTextColor(...C.white);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(15);
  doc.text("HealthMateAI", 14, 11);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(7.5);
  doc.text(
    "AI-Powered Disease Recognition, Early-Diagnosis & Risk-Stratification System",
    14,
    18
  );

  doc.setFont("helvetica", "bold");
  doc.setFontSize(9);
  doc.text("HEALTH ANALYSIS REPORT", pw - 14, 11, { align: "right" });

  doc.setFont("helvetica", "normal");
  doc.setFontSize(7.5);
  doc.text(
    new Date().toLocaleDateString("en-IN", {
      weekday: "long",
      year: "numeric",
      month: "long",
      day: "numeric",
      hour: "2-digit",
      minute: "2-digit",
    }),
    pw - 14,
    18,
    { align: "right" }
  );
};

const addFooter = (doc, pw, ph, pageNum, total) => {
  doc.setFillColor(...C.gray100);
  doc.rect(0, ph - 14, pw, 14, "F");
  doc.setDrawColor(...C.gray300);
  doc.setLineWidth(0.2);
  doc.line(0, ph - 14, pw, ph - 14);

  doc.setFont("helvetica", "italic");
  doc.setFontSize(6.5);
  doc.setTextColor(...C.gray500);
  doc.text(
    "This report is generated by an AI system for informational purposes only and is NOT a substitute for professional medical advice.",
    pw / 2,
    ph - 7,
    { align: "center", maxWidth: pw - 40 }
  );

  doc.setFont("helvetica", "normal");
  doc.setFontSize(7);
  doc.text("healthmateai-app.vercel.app", 14, ph - 3);
  doc.text(`Page ${pageNum} of ${total}`, pw - 14, ph - 3, {
    align: "right",
  });
};




export const generateHealthReport = (prediction) => {
  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
  const pw  = doc.internal.pageSize.getWidth();
  const ph  = doc.internal.pageSize.getHeight();
  const mg  = 14;
  const cw  = pw - mg * 2;

  const disease         = prediction?.predictedDisease           || "Unknown Condition";
  const confidence      = prediction?.confidence                 || 0;
  const risk            = prediction?.mortalityRisk?.risk        || "Unknown";
  const probability     = prediction?.mortalityRisk?.probability || 0;
  const symptoms        = prediction?.symptoms                   || [];
  const vitals          = prediction?.vitalSigns                 || {};
  const recommendations = prediction?.recommendations            || [];
  const createdAt       = prediction?.createdAt
    ? new Date(prediction.createdAt)
    : new Date();
  const mlEnhanced = prediction?.mlEnhanced || false;

  const rc = getRiskColor(risk);
  const rb = getRiskBg(risk);

  


  addHeader(doc, pw);
  let y = 34;

  
  drawRect(doc, mg, y, cw, 16, 3, C.gray100, C.gray300);
  const meta = [
    {
      label: "REPORT DATE",
      value: createdAt.toLocaleDateString("en-IN", {
        day: "2-digit",
        month: "short",
        year: "numeric",
      }),
    },
    {
      label: "REPORT TIME",
      value: createdAt.toLocaleTimeString("en-IN", {
        hour: "2-digit",
        minute: "2-digit",
      }),
    },
    {
      label: "AI ENGINE",
      value: mlEnhanced ? "HuggingFace ML Model" : "Rule-Based Engine",
    },
    {
      label: "PREDICTION ID",
      value: prediction?._id
        ? prediction._id.slice(-8).toUpperCase()
        : "N/A",
    },
  ];
  meta.forEach((m, i) => {
    const mx = mg + 4 + i * (cw / 4);
    doc.setFont("helvetica", "bold");
    doc.setFontSize(6.5);
    doc.setTextColor(...C.gray500);
    doc.text(m.label, mx, y + 6);
    doc.setFont("helvetica", "normal");
    doc.setFontSize(8);
    doc.setTextColor(...C.gray900);
    doc.text(m.value, mx, y + 12);
  });
  y += 22;

  
  drawRect(doc, mg, y, cw, 40, 4, C.primaryLight, C.primary);
  doc.setFillColor(...C.primary);
  doc.rect(mg, y, 1.5, 40, "F");

  doc.setFont("helvetica", "bold");
  doc.setFontSize(7.5);
  doc.setTextColor(...C.primary);
  doc.text("AI PREDICTION RESULT", mg + 5, y + 8);

  doc.setFont("helvetica", "bold");
  doc.setFontSize(19);
  doc.setTextColor(...C.gray900);
  doc.text(disease, mg + 5, y + 20);

  
  const confText = `${confidence}% Confidence`;
  const confW    = doc.getTextWidth(confText) + 8;
  drawRect(doc, mg + 5, y + 24, confW, 9, 2, getConfidenceColor(confidence));
  doc.setFont("helvetica", "bold");
  doc.setFontSize(7.5);
  doc.setTextColor(...C.white);
  doc.text(confText, mg + 5 + confW / 2, y + 30, { align: "center" });

  
  const riskText   = `${risk} Risk Level`;
  const riskBadgeW = doc.getTextWidth(riskText) + 8;
  drawRect(doc, mg + 5 + confW + 4, y + 24, riskBadgeW, 9, 2, rb, rc);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(7.5);
  doc.setTextColor(...rc);
  doc.text(riskText, mg + 5 + confW + 4 + riskBadgeW / 2, y + 30, {
    align: "center",
  });

  
  if (mlEnhanced) {
    const mlText = "ML Enhanced";
    const mlW    = doc.getTextWidth(mlText) + 8;
    drawRect(
      doc,
      mg + 5 + confW + riskBadgeW + 12,
      y + 24,
      mlW,
      9,
      2,
      C.successLight,
      C.success
    );
    doc.setFont("helvetica", "bold");
    doc.setFontSize(7.5);
    doc.setTextColor(...C.success);
    doc.text(
      mlText,
      mg + 5 + confW + riskBadgeW + 12 + mlW / 2,
      y + 30,
      { align: "center" }
    );
  }
  y += 46;

  
  drawRect(doc, mg, y, cw, 16, 3, rb);
  doc.setFillColor(...rc);
  doc.rect(mg, y, 1.5, 16, "F");

  const riskMessages = {
    Low:      "Your health indicators appear stable. Continue monitoring your symptoms.",
    Medium:   "Moderate health concern detected. Consider consulting a healthcare provider.",
    High:     "Elevated health risk identified. Medical consultation is strongly recommended.",
    Critical: "URGENT: Critical health risk detected. Seek immediate medical attention.",
  };
  doc.setFont("helvetica", "bold");
  doc.setFontSize(8.5);
  doc.setTextColor(...rc);
  doc.text(
    riskMessages[risk] || "Health assessment completed.",
    mg + 5,
    y + 7
  );
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.text(`Risk probability: ${Number(probability).toFixed(1)}%`, mg + 5, y + 13);
  y += 22;

  
  const gap   = 5;
  const halfW = (cw - gap) / 2;
  const sympH = Math.max(44, symptoms.length * 7 + 20);

  
  drawRect(doc, mg, y, halfW, sympH, 3, C.white, C.gray300);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(9);
  doc.setTextColor(...C.gray900);
  doc.text("Reported Symptoms", mg + 4, y + 8);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(7.5);
  doc.setTextColor(...C.gray500);
  doc.text(
    `${symptoms.length} symptom${symptoms.length !== 1 ? "s" : ""}`,
    mg + halfW - 4,
    y + 8,
    { align: "right" }
  );
  doc.setDrawColor(...C.gray300);
  doc.setLineWidth(0.2);
  doc.line(mg + 4, y + 11, mg + halfW - 4, y + 11);
  symptoms.forEach((s, i) => {
    const sy = y + 18 + i * 7;
    drawRect(doc, mg + 4, sy - 4, halfW - 8, 6.5, 1.5, C.gray100);
    doc.setFillColor(...C.primary);
    doc.circle(mg + 8, sy - 0.5, 1.2, "F");
    doc.setFont("helvetica", "normal");
    doc.setFontSize(8);
    doc.setTextColor(...C.gray700);
    doc.text(s, mg + 12, sy);
  });
  doc.setFont("helvetica", "bold");
  doc.setFontSize(7.5);
  doc.setTextColor(...C.gray500);
  doc.text(`Total symptoms: ${symptoms.length}`, mg + 4, y + sympH - 4);

  
  const vx = mg + halfW + gap;
  const vitalEntries = [
    {
      label: "Heart Rate",
      value: vitals.heartRate ? `${vitals.heartRate} bpm` : null,
      color: C.danger,
    },
    {
      label: "Blood Pressure",
      value: vitals.bloodPressure ? `${vitals.bloodPressure}` : null,
      color: C.primary,
    },
    {
      label: "Temperature",
      value: vitals.temperature ? `${vitals.temperature}°C` : null,
      color: C.orange,
    },
    {
      label: "Oxygen Level",
      value: vitals.oxygenLevel ? `${vitals.oxygenLevel}%` : null,
      color: C.teal,
    },
  ].filter((v) => v.value !== null);

  drawRect(doc, vx, y, halfW, sympH, 3, C.white, C.gray300);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(9);
  doc.setTextColor(...C.gray900);
  doc.text("Vital Signs Recorded", vx + 4, y + 8);
  doc.setDrawColor(...C.gray300);
  doc.setLineWidth(0.2);
  doc.line(vx + 4, y + 11, vx + halfW - 4, y + 11);

  if (vitalEntries.length === 0) {
    doc.setFont("helvetica", "normal");
    doc.setFontSize(8);
    doc.setTextColor(...C.gray500);
    doc.text("No vital signs recorded", vx + halfW / 2, y + sympH / 2, {
      align: "center",
    });
  } else {
    vitalEntries.forEach((v, i) => {
      const vy = y + 17 + i * 11;
      drawRect(doc, vx + 4, vy - 3, halfW - 8, 9, 2, C.gray100);
      doc.setFont("helvetica", "normal");
      doc.setFontSize(8);
      doc.setTextColor(...C.gray500);
      doc.text(v.label, vx + 7, vy + 2);
      doc.setFont("helvetica", "bold");
      doc.setFontSize(9.5);
      doc.setTextColor(...v.color);
      doc.text(v.value, vx + halfW - 7, vy + 2, { align: "right" });
    });
  }
  y += sympH + 8;

  drawRect(doc, mg, y, cw, 28, 3, rb, rc);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(10);
  doc.setTextColor(...rc);
  doc.text("Detailed Risk Assessment", mg + 4, y + 8);

  const cols3 = [
    { label: "Risk Category",    value: risk },
    { label: "Risk Probability", value: `${Number(probability).toFixed(1)}%` },
    { label: "Confidence Score", value: `${confidence}%` },
  ];
  cols3.forEach((c3, i) => {
    const cx = mg + i * (cw / 3) + cw / 6;
    doc.setFont("helvetica", "normal");
    doc.setFontSize(7.5);
    doc.setTextColor(...C.gray700);
    doc.text(c3.label, cx, y + 16, { align: "center" });
    doc.setFont("helvetica", "bold");
    doc.setFontSize(14);
    doc.setTextColor(...rc);
    doc.text(c3.value, cx, y + 25, { align: "center" });
  });
  y += 34;

  addFooter(doc, pw, ph, 1, 2);

 
  doc.addPage();
  addHeader(doc, pw);
  y = 34;

  doc.setFont("helvetica", "bold");
  doc.setFontSize(12);
  doc.setTextColor(...C.gray900);
  doc.text("Health Recommendations", mg, y);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.setTextColor(...C.gray500);
  doc.text(
    `${recommendations.length} personalised recommendations based on your diagnosis and vital signs`,
    mg,
    y + 6
  );
  y += 13;

  let pageNum = 2;

  recommendations.forEach((rec, i) => {
    const cleanRec = rec.replace(/^⚠️\s*/u, "");
    const isUrgent =
      cleanRec.toLowerCase().includes("urgent") ||
      cleanRec.toLowerCase().includes("emergency") ||
      cleanRec.toLowerCase().includes("immediate");
    const lines = doc.splitTextToSize(cleanRec, cw - 18);
    const boxH  = lines.length * 5 + 10;

    if (y + boxH > ph - 22) {
      addFooter(doc, pw, ph, pageNum, pageNum + 1);
      doc.addPage();
      addHeader(doc, pw);
      y = 34;
      pageNum += 1;
    }

    const bg     = isUrgent ? C.dangerLight : i % 2 === 0 ? C.white : C.gray100;
    const border = isUrgent ? C.danger      : C.gray300;
    drawRect(doc, mg, y, cw, boxH, 2, bg, border);

    if (isUrgent) {
      doc.setFillColor(...C.danger);
      doc.rect(mg, y, 1.5, boxH, "F");
    }

    doc.setFillColor(...(isUrgent ? C.danger : C.primary));
    doc.circle(mg + 7, y + boxH / 2, 4, "F");
    doc.setFont("helvetica", "bold");
    doc.setFontSize(7);
    doc.setTextColor(...C.white);
    doc.text(`${i + 1}`, mg + 7, y + boxH / 2 + 1, { align: "center" });

    doc.setFont("helvetica", isUrgent ? "bold" : "normal");
    doc.setFontSize(8.5);
    doc.setTextColor(...(isUrgent ? C.danger : C.gray900));
    doc.text(lines, mg + 15, y + 7);

    y += boxH + 3;
  });

  
  if (y + 36 > ph - 22) {
    addFooter(doc, pw, ph, pageNum, pageNum + 1);
    doc.addPage();
    addHeader(doc, pw);
    y = 34;
    pageNum += 1;
  }

  y += 6;
  drawRect(doc, mg, y, cw, 34, 3, C.warningLight, C.warning);
  doc.setFillColor(...C.warning);
  doc.rect(mg, y, 1.5, 34, "F");

  doc.setFont("helvetica", "bold");
  doc.setFontSize(9);
  doc.setTextColor(...C.warning);
  doc.text("Important Medical Disclaimer", mg + 5, y + 8);

  const disclaimers = [
    {
      text: "This prediction is generated by an AI system for informational purposes only.",
      bold: false,
    },
    {
      text: "NOT a substitute for professional medical advice, diagnosis, or treatment.",
      bold: true,
    },
    {
      text: "Always consult with qualified healthcare professionals for accurate diagnosis.",
      bold: false,
    },
    {
      text: "In case of emergency, contact your local emergency services immediately.",
      bold: false,
    },
  ];
  disclaimers.forEach((d, i) => {
    doc.setFont("helvetica", d.bold ? "bold" : "normal");
    doc.setFontSize(8);
    doc.setTextColor(...C.gray700);
    doc.text(`• ${d.text}`, mg + 8, y + 15 + i * 5.5);
  });

  addFooter(doc, pw, ph, pageNum, pageNum);

 
  const safeName = disease.replace(/[^a-zA-Z0-9]/g, "_");
  const dateStr  = new Date().toISOString().split("T")[0];
  doc.save(`HealthMateAI_Report_${safeName}_${dateStr}.pdf`);
};
